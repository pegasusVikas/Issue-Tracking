package com.example.model;

public class OverAllStatAdmin {
	
	private int users;
	private int developers;
	private int new_issues;
	private int solved_issues;
	private int active_issues;
	
	public int getUsers() {
		return users;
	}
	public void setUsers(int users) {
		this.users = users;
	}
	public int getDevelopers() {
		return developers;
	}
	public void setDevelopers(int developers) {
		this.developers = developers;
	}
	public int getNew_issues() {
		return new_issues;
	}
	public void setNew_issues(int new_issues) {
		this.new_issues = new_issues;
	}
	public int getSolved_issues() {
		return solved_issues;
	}
	public void setSolved_issues(int solved_issues) {
		this.solved_issues = solved_issues;
	}
	public int getActive_issues() {
		return active_issues;
	}
	public void setActive_issues(int active_issues) {
		this.active_issues = active_issues;
	}
	
	

}
